     * @param  \App\Models\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        if ($user->isDirty(['synced_with_batch_api', 'password', 'remember_token'])) {
            return;
        }
        $user->synced_with_batch_api = false;
        $user->saveQuietly();
    }

